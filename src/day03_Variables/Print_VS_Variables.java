package day03_Variables;
/*
\n:new line
\t":paragraph space
\"

 */
public class Print_VS_Variables {

    public static void main(String[] args) {

        System.out.println("Hello Batch 23");  //appens new line after printing
        System.out.println("How are you today");

        System.out.println("Hello everyone \nhow are you all today?");
        System.out.print("Hello everone\nMy name is Aykut\nI am living los angeles");
        System.out.println("-----------------------------------------------");

        System.out.println("\tToday is a great day to study java");

        System.out.println("----------------------------------------------------");

        System.out.println("My favourite TV series is \"Game of thrones\"");

        System.out.println("\\");


    }



}
